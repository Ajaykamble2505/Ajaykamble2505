public class If_Else {
    public static void main(String args[]) {
       
        int a=1234;
        int b=1345;
        
        System.out.println("a=" +a);
        System.out.println("b=" +b);
        
        if(a>b) {
            System.out.println("ans=" +a);
        }
        else {
            System.out.println("ans=" +b);
        }
        
    }
}